package com.lp2.foroPesca_spring.modelo;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.FetchType;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;
import javax.validation.constraints.Size;

/**
 *
 * @author Addis Flerida Mu√±oz
 */
@Entity
@Table(name = "peces")
@NamedQueries({
    @NamedQuery(name = "Peces.findAll", query = "SELECT p FROM Peces p")})
public class Peces implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "Id")
    private Integer id;
    @Size(max = 255)
    @Column(name = "Tipo")
    private String tipo;
    @Size(max = 255)
    @Column(name = "Especie")
    private String especie;
    @ManyToMany(mappedBy = "pecesList", fetch = FetchType.LAZY)
    private List<Aguas> aguasList;

    public Peces() {
    }

    public Peces(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getEspecie() {
        return especie;
    }

    public void setEspecie(String especie) {
        this.especie = especie;
    }

    public List<Aguas> getAguasList() {
        return aguasList;
    }

    public void setAguasList(List<Aguas> aguasList) {
        this.aguasList = aguasList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Peces)) {
            return false;
        }
        Peces other = (Peces) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return " "+especie+" ";
    }

}
